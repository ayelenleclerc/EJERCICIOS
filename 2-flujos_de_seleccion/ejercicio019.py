"""Escribir un programa que permita ingresar dos números enteros e indicar si son iguales o distintos."""

numero1 = int(input("Ingrese un número entero: "))
numero2 = int(input("Ingrese otro número entero: "))

if numero1 == numero2:
    print(f"Los números {numero1} y {numero2} son iguales")
else:
    print(f"Los números {numero1} y {numero2} son distintos")
