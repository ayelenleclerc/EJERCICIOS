"""Escribir un programa que permita ingresar dos cadenas de caracteres e indicar si son iguales o distintas."""

cadena1 = input("Ingrese una cadena de caracteres: ")
cadena2 = input("Ingrese otra cadena de caracteres: ")

if cadena1 == cadena2:
    print(f"Las cadenas {cadena1} y {cadena2} son iguales")
else:
    print(f"Las cadenas {cadena1} y {cadena2} son distintas")
